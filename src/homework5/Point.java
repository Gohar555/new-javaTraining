public class Point {

    private int point;

    public void setFirstPoint(int x1, int x2) {
        this.point = x1;
        this.point = x2;
    }

    public int getFirstPoint() {
        return point;
    }

    public void setSecondPoint(int y1, int y2) {
        this.point = y1;
        this.point = y2;
    }

    public int getSecondPoint() {
        return point;
    }
}